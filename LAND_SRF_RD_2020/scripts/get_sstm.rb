def get_sstm(wndspd,snwfll,airtmp,wx)
  return LACK_VALUE_8 if wndspd == LACK_VALUE_16
  return LACK_VALUE_8 if airtmp == LACK_VALUE_16
  if snwfll == LACK_VALUE_16
    # 非降水(100/200) 雨(300)
    if wx == 100 || wx == 200 || wx == 300
      return 0
    end
    return LACK_VALUE_8
  end
  ws = wndspd.round
  sf = snwfll
  at = airtmp
  sf = 10 if sf > 10
  ws = 10 if ws > 10
  if at <= -2.6
    return $sstm_table1_1[sf][ws]
  else
    return $sstm_table1_2[sf][ws]
  end 
end
